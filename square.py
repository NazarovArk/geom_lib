
def area(a):
    '''
    Calculates the area of ​​a square with side a.

        Parameters:
            a (float): square side

        Returns:
            square_area (float): area of ​​a square with side a
    '''
    return a * a


def perimeter(a):
    '''
    Calculates the perimeter of ​​a square with side a.

        Parameters:
            a (float): square side

        Returns:
            square_perimeter (float): perimeter of ​​a square with side a
    '''
    return 4 * a
